const translations: Record<Language, Dict> = { en, ar, tr };

export type TFunc = (key: string, params?: Record<string, string | number>) => string;

function interpolate(template: string, params?: Record<string, string | number>): string {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, k: string) => (k in params ? String(params[k]) : `{${k}}`));
}

export function makeT(lang: Language): TFunc {
  const dict = translations[lang] ?? en;
  return (key, params) => interpolate(dict[key] ?? en[key] ?? key, params);
}

const I18nContext = createContext<{ t: TFunc; lang: Language }>({ t: makeT('en'), lang: 'en' });

export function I18nProvider(props: { lang: Language; children: ReactNode }) {
  return <I18nContext.Provider value={{ t: makeT(props.lang), lang: props.lang }}>{props.children}</I18nContext.Provider>;
}

export function useT(): { t: TFunc; lang: Language } {
  return useContext(I18nContext);
}
